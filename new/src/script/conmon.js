export const SnUniqueId = (length = 6) => {
    let timestamp = +new Date;

    let _getRandomInt = function (min, max) {
        return Math.floor(Math.random() * (max - min + 1)) + min;
    };

    let ts = timestamp.toString();
    let parts = ts.split("").reverse();
    let id = "";
    for (let i = 0; i < length; ++i) {
        let index = _getRandomInt(0, parts.length - 1);
        id += parts[index];
    }
    return id;
};

export const SnLoadingState = (state, className, idName = null) => {
    let actions = document.querySelectorAll(`.${className}`);
    let submitBtn = document.getElementById(idName);
    if (state) {
        if (submitBtn) {
            submitBtn.setAttribute("disabled", "disabled");
            submitBtn.classList.add("loading");
        }
        if (actions) {
            actions.forEach((item) => {
                item.setAttribute("disabled", "disabled");
            });
        }
    } else {
        if (submitBtn) {
            submitBtn.removeAttribute("disabled");
            submitBtn.classList.remove("loading");
        }
        if (actions) {
            actions.forEach((item) => {
                item.removeAttribute("disabled");
            });
        }
    }
}

export const SnFormatNumber = (number, presition = 2) => {
    if (isNaN(number)) {
        return 0;
    }

    return parseFloat(number).toLocaleString('es-US', {
        style: 'decimal', minimumFractionDigits: presition, maximumFractionDigits: presition
    });
}

